
public class Date {

	private int _day;
	private int _month;
	private int _year;
	
	
	//GETTERS
	public int getDay() {
		return _day;
	}
	public int getMonth() {
		return _month;
	}
	public int getYear() {
		return _year;
	}
	
	//SETTERS
	public void set_day(int day) {
		_day = day;
	}
	public void set_month(int month) {
		_month = month;
	}
	public void set_year(int year) {
		_year = year;
	}
	
	
}
